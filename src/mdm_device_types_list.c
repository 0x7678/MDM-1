#include	<stdio.h>
#include	<stdlib.h>
#include	<unistd.h>
#include	<string.h>
#include	<mdm.h>

/*******************************************************************************
 * Constants.
 ******************************************************************************/

/*******************************************************************************
 * Global variables.
 ******************************************************************************/

/*******************************************************************************
 * Forward Declarations.
 ******************************************************************************/
int main(int argc, char *argv[]);

/*******************************************************************************
 * CODE STARTS.
 ******************************************************************************/
/*!
 * Main entry point.
 */
int
main(int argc, char *argv[])
{
	int i = 0;
	const char *device_name;
	
	MDM_LOG("Start.");
	while((device_name = mdm_device_2string(i)) != NULL)
	{
		fprintf(stdout, "(%.2d) - %s\n", i, device_name);
		i++;
	}

	MDM_LOG("Done.");
	exit(EXIT_SUCCESS);
}
